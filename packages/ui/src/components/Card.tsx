/**
 * Card Component - Web version
 * Matches @nextsparkjs/core implementation
 */
import * as React from "react";
import { cn } from "../utils";

export interface CardProps extends React.HTMLAttributes<HTMLDivElement> {}

const Card = React.forwardRef<HTMLDivElement, CardProps>(
  ({ className, ...props }, ref) => (
    <div
      ref={ref}
      className={cn(
        "rounded-xl border bg-card text-card-foreground shadow",
        className
      )}
      {...props}
    />
  )
);
Card.displayName = "Card";

export interface PressableCardProps extends React.HTMLAttributes<HTMLDivElement> {
  onClick?: () => void;
}

const PressableCard = React.forwardRef<HTMLDivElement, PressableCardProps>(
  ({ className, onClick, ...props }, ref) => (
    <div
      ref={ref}
      role="button"
      tabIndex={0}
      onClick={onClick}
      onKeyDown={(e) => {
        if (e.key === "Enter" || e.key === " ") {
          e.preventDefault();
          onClick?.();
        }
      }}
      className={cn(
        "rounded-xl border bg-card text-card-foreground shadow cursor-pointer transition-opacity hover:opacity-80 active:opacity-70",
        className
      )}
      {...props}
    />
  )
);
PressableCard.displayName = "PressableCard";

